    private void mni_themActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mni_themActionPerformed
        //kiểm tra người dùng click mấy lần
        String MaSP, TenSP;
        int row = tbl_SanPham.getSelectedRow();
        if (evt.getClickCount()==2) {
            MaSP = tbl_SanPham.getValueAt(row, 0).toString();
            TenSP = tbl_SanPham.getValueAt(row,1).toString();
            //tạo panel thông tin mua hàng
            pnl_thongtinmuahang frmMH = new pnl_thongtinmuahang();
            //show hộp thoại xác nhận
            int luachon = JOptionPane.showConfirmDialog(new JFrame(), frmMH, "Nhập Thông Tin Mua Hàng", JOptionPane.YES_NO_OPTION);
            
            // kiểm tra xem người dùng bấm vào yes hay no
            if (luachon == JOptionPane.YES_OPTION) {
                int check = BLL_HoaDon.CheckSP(TenSP, tbl_CTHD);
                // giảm số lượng của bàn sản phẩm khi mua hàng
                if (check == 0) {
                    System.out.println("Sản Phẩm Đã Tồn tại");
                        int giamSlSP = Integer.parseInt(tbl_SanPham.getValueAt(row, 2).toString()) - Integer.parseInt(frmMH.spn_SoLluong.getValue().toString());
                    //set lại số lượng sản phẩm trong bảng sản phẩm khi mua
                    if (giamSlSP < 0) {
                        Lab05.ThongBao("Sản phẩm trong kho không đủ", "Thông báo lỗi");
                        return;
                    }
                    tbl_SanPham.setValueAt(giamSlSP, row, 2);
                    int SoLuong = Integer.parseInt(frmMH.spn_SoLluong.getValue().toString());
                    String GhiChu = frmMH.txt_Ghichu.getText();
                    BLL_HoaDon.themVaoCTHD_SL(tbl_CTHD, SoLuong, TenSP);
                
                }else{
                    System.out.println("Sản phẩm Chưa Tồn Tại");
                    int giamSlSP = Integer.parseInt(tbl_SanPham.getValueAt(row, 2).toString()) - Integer.parseInt(frmMH.spn_SoLluong.getValue().toString());
                    //set lại số lượng sản phẩm trong bảng sản phẩm khi mua
                    if (giamSlSP < 0) {
                        Lab05.ThongBao("Sản phẩm trong kho không đủ", "Thông báo lỗi");
                        return;
                    }
                    tbl_SanPham.setValueAt(giamSlSP, row, 2);
                
                    int SoLuong = Integer.parseInt(frmMH.spn_SoLluong.getValue().toString());
                    String GhiChu = frmMH.txt_Ghichu.getText();
                    BLL_HoaDon.ThemVaoCTHD(tbl_CTHD,MaSP,SoLuong,GhiChu);
                }
            }
        }else{
        
        }
        lbl_TongTien.setText(BLL_HoaDon.TinhTongTien(tbl_CTHD, 5));
    }//GEN-LAST:event_mni_themActionPerformed

