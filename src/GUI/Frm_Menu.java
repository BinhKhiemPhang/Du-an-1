    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        //Form Quyền
        BLL_Quyen.DLTable(tbl_Quyen, DAL_Quyen.GetALL_Quyen());
        //Form Nhân Viên
            //Table nhân viên
        BLL_NhanVien.DLTable(tbl_nhanvien, DAL_NhanVien.GetALL_NhanVien());
            //Combobox Quyền trong form nhân viên
        BLL_Combobox.LayDuLieuCombobox(DAL_Quyen.GetALL_Quyen(), cbb_nv_quyen, "TENQUYEN", "MAQUYEN");
        //Form Đại Lý
        BLL_DaiLy.DLTable(tbl_daily, DAL_DaiLy.GetALL_DaiLy());
        //Form nhà xuất bản
        BLL_NhaXuatBan.DLTable(tbl_nhaxuatban, DAL_NhaXuatBan.GetALL_NhaXuatBan());
        //From Tác Giả
        BLL_TacGia.DLTable(tbl_tacgia, DAL_TacGia.GetALL_TacGia());
        //From thể loại
        BLL_TheLoai.DLTable(tbl_theloai, DAL_TheLoai.GetALL_TheLoai());
        // Form sách
            // Combobox tác giả
        BLL_Combobox.LayDuLieuCombobox(DAL_TacGia.GetALL_TacGia(), cbb_sach_tacgia, "TENTG", "MATG");
            // Combobox thể loại
        BLL_Combobox.LayDuLieuCombobox(DAL_TheLoai.GetALL_TheLoai(), cbb_sach_theloai, "TENTL", "MATL");
            //table sách
        BLL_Sach.DLTable(tbl_sach, DAL_Sach.GetALL_Sach());
        //From Xuất Kho
        BLL_HoaDonXuat.DLTable(tbl_spxuat, DAL_Sach.GetALL_Sach_TG_TL());
        //đổ dl vào số phiếu
        txt_xuat_sophieu.setText(BLL_HoaDonXuat.SoHoaDon());
        //đổ dl vào ngày tạo
        txt_xuat_ngaytao.setText(BLL_HoaDonXuat.NgayTaoHD());
        //đổ dl vào cbb nhân viên
        BLL_Combobox.LayDuLieuCombobox(DAL_NhanVien.GetALL_NhanVien(), cbb_xuat_nv, "TENNV", "MANV");
        // đổ dữ liệu vào cbb đại lý
        BLL_Combobox.LayDuLieuCombobox(DAL_DaiLy.GetALL_DaiLy(), cbb_xuat_KH, "TENDAILY", "MADAILY");
        //Biến lưu mã khách hàng mỗi khi chọn combobox
            String maKH = BLL_Combobox.getSelectedItemID(cbb_xuat_KH);
            DTO_DaiLy kh = BLL_HoaDonXuat.layDLKhachHangTheoCBB(maKH);
            //Gán thông tin khách hàng với đối tượng kh ở trên
            txt_xuat_diachi.setText(kh.getDiaChi());
            txt_xuat_sdt.setText(kh.getSoDienThoai());
    }//GEN-LAST:event_formWindowOpened

